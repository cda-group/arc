use lazy_static::lazy_static;
use std::collections::HashMap;
use std::str;
use trie_rs::*;

lazy_static! {
    static ref TRIE: Trie<u8> = {
        let mut trie = TrieBuilder::new();
        for (key, _) in LATEX.iter() {
            trie.push(key);
        }
        trie.build()
    };
    static ref MAP: HashMap<&'static str, &'static str> =
        LATEX.iter().copied().collect::<HashMap<&str, &str>>();
}

pub struct Completer;

impl Completer {
    pub fn _complete(item: &str) -> impl Iterator<Item = (String, String)> {
        TRIE.predictive_search(item)
            .into_iter()
            .map(|u8s: Vec<u8>| {
                let key = str::from_utf8(&u8s).unwrap();
                let val = *MAP.get(key).unwrap();
                (key.to_string(), val.to_string())
            })
    }
}

